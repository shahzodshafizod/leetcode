package twopointers

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

// go test -v -count=1 ./twopointers/ -run ^TestMoveZeroes$
func TestMoveZeroes(t *testing.T) {
	for _, tc := range []struct {
		nums     []int
		modified []int
	}{
		{nums: []int{0}, modified: []int{0}},
		{nums: []int{1, 0, 0}, modified: []int{1, 0, 0}},
		{nums: []int{0, 1, 0, 3, 12}, modified: []int{1, 3, 12, 0, 0}},
		{nums: []int{0, 0, 0, 0, 0, 0, 1}, modified: []int{1, 0, 0, 0, 0, 0, 0}},
		{nums: []int{1, 2, 3, 0, 0, 0, 4, 5}, modified: []int{1, 2, 3, 4, 5, 0, 0, 0}},
		{nums: []int{4, 2, 4, 0, 0, 3, 0, 5, 1, 0}, modified: []int{4, 2, 4, 3, 5, 1, 0, 0, 0, 0}},
		{nums: []int{45192, 0, -659, -52359, -99225, -75991, 0, -15155, 27382, 59818, 0, -30645, -17025, 81209, 887, 64648}, modified: []int{45192, -659, -52359, -99225, -75991, -15155, 27382, 59818, -30645, -17025, 81209, 887, 64648, 0, 0, 0}},
		// {nums: []int{16601, 78714, 11653, -45162, 0, -22859, 0, 36007, 19143, -91750, 0, -45361, -10715, 46528, -91518, -36985, 59578, 76628, -87592, 89803, 0, -41430, 44290, 59831, 41824, -30916, -6521, 61614, 46035, 39346, 0, 0, 32417}, modified: []int{16601, 78714, 11653, -45162, -22859, 36007, 19143, -91750, -45361, -10715, 46528, -91518, -36985, 59578, 76628, -87592, 89803, -41430, 44290, 59831, 41824, -30916, -6521, 61614, 46035, 39346, 32417, 0, 0, 0, 0, 0, 0}},
	} {
		moveZeroes(tc.nums)
		assert.Equal(t, tc.modified, tc.nums)
	}
}
