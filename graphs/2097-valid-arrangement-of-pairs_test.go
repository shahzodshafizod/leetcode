package graphs

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

// go test -v -count=1 ./graphs/ -run ^TestValidArrangement$
func TestValidArrangement(t *testing.T) {
	for _, tc := range []struct {
		pairs       [][]int
		arrangement [][]int
	}{
		{pairs: [][]int{{1, 3}, {3, 2}, {2, 1}}, arrangement: [][]int{{1, 3}, {3, 2}, {2, 1}}},
		{pairs: [][]int{{1, 2}, {1, 3}, {2, 1}}, arrangement: [][]int{{1, 2}, {2, 1}, {1, 3}}},
		{pairs: [][]int{{5, 1}, {4, 5}, {11, 9}, {9, 4}}, arrangement: [][]int{{11, 9}, {9, 4}, {4, 5}, {5, 1}}},
		{pairs: [][]int{{299, 1}, {1, 2}, {1, 3}, {2, 1}, {3, 1}}, arrangement: [][]int{{299, 1}, {1, 3}, {3, 1}, {1, 2}, {2, 1}}},
		{pairs: [][]int{{1, 3}, {3, 9}, {9, 4}, {4, 1}, {1, 4}, {4, 6}, {6, 3}, {3, 1}, {1, 456}}, arrangement: [][]int{{1, 4}, {4, 6}, {6, 3}, {3, 1}, {1, 3}, {3, 9}, {9, 4}, {4, 1}, {1, 456}}},
		// {pairs: [][]int{{299, 1}, {1, 2}, {1, 3}, {3, 5}, {5, 8}, {8, 3}, {3, 7}, {7, 6}, {6, 9}, {9, 3}, {3, 6}, {6, 7}, {7, 3}, {3, 8}, {8, 5}, {5, 3}, {3, 1}, {2, 1}}, arrangement: [][]int{{299, 1}, {1, 3}, {3, 8}, {8, 5}, {5, 3}, {3, 6}, {6, 7}, {7, 3}, {3, 7}, {7, 6}, {6, 9}, {9, 3}, {3, 5}, {5, 8}, {8, 3}, {3, 1}, {1, 2}, {2, 1}}},
		// {pairs: [][]int{{5, 13}, {10, 6}, {11, 3}, {15, 19}, {16, 19}, {1, 10}, {19, 11}, {4, 16}, {19, 9}, {5, 11}, {5, 6}, {13, 5}, {13, 9}, {9, 15}, {11, 16}, {6, 9}, {9, 13}, {3, 1}, {16, 5}, {6, 5}}, arrangement: [][]int{{4, 16}, {16, 5}, {5, 6}, {6, 5}, {5, 11}, {11, 16}, {16, 19}, {19, 9}, {9, 13}, {13, 5}, {5, 13}, {13, 9}, {9, 15}, {15, 19}, {19, 11}, {11, 3}, {3, 1}, {1, 10}, {10, 6}, {6, 9}}},
	} {
		arrangement := validArrangement(tc.pairs)
		assert.Equal(t, tc.arrangement, arrangement)
	}
}
